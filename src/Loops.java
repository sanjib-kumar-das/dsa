import java.util.Scanner;
public class Loops {
    public static void main(String[] args) {
        int num=10;
//        FOR LOOP
        for(int i=1; i<=num; i++){
            System.out.println(i);
        }

//        WHILE LOOP
        int x=1;
        while(x<=10){
            System.out.println(x);
            x++;

        }
    }
}
